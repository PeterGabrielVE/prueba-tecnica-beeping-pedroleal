    public function calcula_total_orders(){
        DB::table('orders')
            ->select('order_id', 'order_line_qty', 'product_cost')
            ->get()
            ->each(function ($order) {
                $order->total_cost = $order->order_line_qty * $order->product_cost;
            });

        // Imprime el resultado en la terminal
